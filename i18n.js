let activeLanguage = DEFAULT_LANGUAGE;

function normalizeLanguage(language) {
  return SUPPORTED_LANGUAGES.includes(language) ? language : DEFAULT_LANGUAGE;
}

function interpolate(template, params) {
  return String(template).replace(/\{([a-zA-Z0-9_]+)\}/g, (match, key) => (
    Object.hasOwn(params, key) ? String(params[key]) : match
  ));
}

export function resolveInitialLanguage({ search = "", stored = null } = {}) {
  const queryLanguage = new URLSearchParams(search).get("lang");
  if (SUPPORTED_LANGUAGES.includes(queryLanguage)) return queryLanguage;
  return normalizeLanguage(stored);
}

export function getLanguage() {
  return activeLanguage;
}

export function t(key, params = {}, fallback = key) {
  const template = messages[activeLanguage]?.[key] ?? messages[DEFAULT_LANGUAGE]?.[key] ?? fallback;
  return interpolate(template, params);
}

export function localizedValue(value, language = activeLanguage) {
  if (value === null || value === undefined) return "";
  if (typeof value !== "object" || Array.isArray(value)) return String(value);
  const normalized = normalizeLanguage(language);
  return String(value[normalized] ?? value.en ?? value.zh ?? "");
}

export function applyTranslations(root = document) {
  root.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = t(element.dataset.i18n);
  });
  ["aria-label", "title", "placeholder", "content"].forEach((attribute) => {
    const dataName = `i18n${attribute.split("-").map((part) => part[0].toUpperCase() + part.slice(1)).join("")}`;
    root.querySelectorAll(`[data-${dataName.replace(/[A-Z]/g, (letter) => `-${letter.toLowerCase()}`)}]`).forEach((element) => {
      element.setAttribute(attribute, t(element.dataset[dataName]));
    });
  });
  root.querySelectorAll("[data-language]").forEach((button) => {
    const selected = button.dataset.language === activeLanguage;
    button.classList.toggle("is-active", selected);
    button.setAttribute("aria-pressed", String(selected));
  });
}

export function setLanguage(language, { persist = true, notify = true } = {}) {
  activeLanguage = normalizeLanguage(language);
  if (typeof document !== "undefined") {
    document.documentElement.lang = activeLanguage === "zh" ? "zh-CN" : "en";
    applyTranslations(document);
  }
  if (persist && typeof window !== "undefined") {
    window.localStorage.setItem(LANGUAGE_STORAGE_KEY, activeLanguage);
  }
  if (notify && typeof window !== "undefined") {
    window.dispatchEvent(new CustomEvent("i18n:change", { detail: { language: activeLanguage } }));
  }
  return activeLanguage;
}

export function initI18n() {
  const language = typeof window === "undefined"
    ? DEFAULT_LANGUAGE
    : resolveInitialLanguage({
      search: window.location.search,
      stored: window.localStorage.getItem(LANGUAGE_STORAGE_KEY),
    });
  return setLanguage(language, { persist: false, notify: false });
}

export function bindLanguageControls(root = document) {
  root.querySelectorAll("[data-language]").forEach((button) => {
    button.addEventListener("click", () => setLanguage(button.dataset.language));
  });
}
